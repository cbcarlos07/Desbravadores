    private void preencherComboBairro(){
        combo_bairros.removeAllItems();
        Bairro_Controller bc = new Bairro_Controller();
        Bairro bairro = new Bairro();
        List lista = bc.getBairroList("");
        Iterator iterator = lista.iterator();
        combo_bairros.addItem("SELECIONE");
        while (iterator.hasNext()){
            bairro = (Bairro) iterator.next();
            combo_bairros.addItem(bairro);
        }
        
    }
    
    private void preencherComboCidade(){
        combo_cidade.removeAllItems();
        Cidade_Controller cc = new Cidade_Controller();
        Cidade cidade = new Cidade();
        List lista = cc.getCidadeList("");
        Iterator iterator = lista.iterator();
        combo_cidade.addItem("SELECIONE");
        while (iterator.hasNext()){
            cidade = (Bairro) iterator.next();
            combo_cidade.addItem(cidade);
        }
        
    }
