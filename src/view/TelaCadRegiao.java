    private void acao(){
        if(codigoRegiao > 0){
            alterar();
        }else{
            salvar();
        }
    }
    
    private void salvar(){
        regiao_Controller = new Regiao_Controller();
        regiao = new Regiao();
        
        regiao.setNome_Regiao(jt_regiao.getText());
        regiao.setDistrito(new Distrito());
        Distrito distrito = (Distrito) combo_dados.getSelectedItem();
        regiao.getDistrito().setCodigo(distrito.getCodigo());
        regiao.setSn_principal(getChecked());
        boolean teste = regiao_Controller.inserir(regiao);
        if(teste){
            TelaAdmRegiao.pesquisaRegiao("");
            mensagem("Cadastro efetuado com sucesso! Cadastrar outro?", "Parab√©ns", 1);
        }else{
            
            mensagem(MensagemErro.mensagem, "Opa!", 3);
        }
    }
    
    private void alterar(){
        regiao_Controller = new Regiao_Controller();
        regiao = new Regiao();
        
        regiao.setRegiao_Codigo(codigoRegiao);
        regiao.setNome_Regiao(jt_regiao.getText());
        regiao.setDistrito(new Distrito());
        Distrito distrito = (Distrito) combo_dados.getSelectedItem();
        regiao.getDistrito().setCodigo(distrito.getCodigo());
        regiao.setSn_principal(getChecked());
        boolean teste = regiao_Controller.alterar(regiao);
        if(teste){
            TelaAdmRegiao.pesquisaRegiao("");
            mensagem("Altera√ß√£o efetuada com sucesso!", "Parab√©ns!", 2);
        }else{
            String mensagem = "<html><center>Verifique os dados cadastrados<br>"+MensagemErro.mensagem+"</center></html>";
            mensagem(mensagem, "Opa!", 3);
        }
    }
    
    
      private void mensagem(String mensagem, String titulo, int opcao){
          switch(opcao){
              case 1:
                  int option = JOptionPane.showConfirmDialog(null, mensagem, titulo, JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                  if(option == JOptionPane.YES_OPTION){
                     limpar();
                  }else{
                      dispose();
                  }
                  break;
              case 2:
                  JOptionPane.showMessageDialog(null, mensagem, titulo, JOptionPane.INFORMATION_MESSAGE);
                  dispose();
                  break;
              case 3:
                  JOptionPane.showMessageDialog(null, mensagem, titulo, JOptionPane.ERROR_MESSAGE);
                  break;
          }
      }
      
      private void limpar(){
          jt_regiao.setText("");
          jt_regiao.requestFocus();
          
      }
      
      private void preencherDados(int codigo){
        regiao = new Regiao();
        regiao_Controller = new Regiao_Controller();
        regiao = regiao_Controller.getRegiao(codigo);
        jt_regiao.setText(regiao.getNome_Regiao());
        if(regiao.getSn_principal().equals("S")){
            jchk_principal.setSelected(true);
        }
        
        int j = 0;
        for (int i = 0; i < combo_dados.getItemCount(); i++){
           String combo = combo_dados.getItemAt(i).toString();
           if(combo.equals(regiao.getDistrito().getDistrito())){
               j = i;
               break;
           }
        }
        
        combo_dados.setSelectedIndex(j);
    }
      
      private void preencherCombo(){
          Distrito distrito = null;
          Distrito_Controller distrito_Controller = new Distrito_Controller();
          List list = distrito_Controller.getDistritoList("");
          Iterator iterator = list.iterator();
          while(iterator.hasNext()){
              distrito = (Distrito) iterator.next();
              combo_dados.addItem(distrito);
          }
      }
      
      private String getChecked(){
          if(jchk_principal.isSelected()){
              return "S";
          }else{
              return "N";
          }
      }
